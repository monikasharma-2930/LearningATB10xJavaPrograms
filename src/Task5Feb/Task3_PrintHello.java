package Task5Feb;

public class Task3_PrintHello {
    public static void main(String[] args) {
        System.out.println("Print using For loop:");
        for(int i=0;i<5;i++)
            System.out.println("Hello");

        System.out.println("Print using While loop");
        int j=0;
        while(j<5) {
            System.out.println("Hello");
            j++;
        }

    }
}
