package Task5Feb;

public class Task1_Print1To10 {
    public static void main(String[] args) {
        //for loop
        for(int i=1;i<=10;i++){
            System.out.println(i);
        }

        //while loop
        int j=0;
        while(j<10){
            j++;
            System.out.println(j);
        }


    }

}
